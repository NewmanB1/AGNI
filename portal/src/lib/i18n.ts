function detectLanguage(): string {
  if (typeof localStorage !== 'undefined') {
    const stored = localStorage.getItem('agni_lang');
    if (stored && STRINGS[stored]) return stored;
  }
  if (typeof navigator !== 'undefined') {
    const nav = (navigator.language || '').split('-')[0];
    if (STRINGS[nav]) return nav;
  }
  return 'en';
}

export const currentLang = writable(detectLanguage());

export function setLanguage(lang: string): void {
  if (!STRINGS[lang]) return;
  if (typeof localStorage !== 'undefined') {
    localStorage.setItem('agni_lang', lang);
  }
  currentLang.set(lang);
}

export const availableLanguages = Object.keys(STRINGS);

export const t = derived(currentLang, (lang) => {
  const table = STRINGS[lang] || STRINGS.en;
  return (key: string, vars?: Record<string, string | number>): string => {
    let str = table[key] || STRINGS.en[key] || key;
    if (vars) {
      for (const k of Object.keys(vars)) {
        str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), String(vars[k]));
      }
    }
    return str;
  };
});
